class IndiceInvertido:
    def __init__(self,id_doc,palavra,qtd):
        self.id_doc=id_doc
        self.qtd=qtd
        self.palavra=palavra


    def getId_Doc(self):
        return self.id_doc

    def getQtd(self):
        return self.qtd

    def getPalavra(self):
        return self.palavra



